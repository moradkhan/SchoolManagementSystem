    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        double[] R = new double[14];
        R[0] = Double.parseDouble(bangla.getText());
        R[1] = Double.parseDouble(english.getText());
        R[2] = Double.parseDouble(math.getText());
        R[3] = Double.parseDouble(biology.getText());
        R[4] = Double.parseDouble(history.getText());
        R[5] = Double.parseDouble(socialscience.getText());
        R[6] = Double.parseDouble(generalscience.getText());
        R[7] = Double.parseDouble(physics.getText());
        R[8] = Double.parseDouble(chemistry.getText());

        R[9] = (R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6] + R[7] + R[8]) / 9;
        R[10] = R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6] + R[7] + R[8];
        String avarage = String.format("%.0f", R[9]);
        percentage.setText(String.valueOf(avarage));
        String total = String.format("%.0f", R[10]);
        totalmarks.setText(total);

        if (R[10] >= 720 && R[10] <= 900) {
            grade.setText("A+");
        }
        if (R[10] >= 630 && R[10] < 720) {
            grade.setText("A");
        }
        if (R[10] >= 540 && R[10] < 630) {
            grade.setText("A-");
        }
        if (R[10] >= 495 && R[10] < 540) {
            grade.setText("B");
        }
        if (R[10] >= 405 && R[10] < 495) {
            grade.setText("C");
        }
        if (R[10] >= 315 && R[10] < 405) {
            grade.setText("D");
        }
        if (R[10] < 315) {
            grade.setText("F");
        }
        if (R[0] < 33 && R[1] < 33 && R[2] < 33 && R[3] < 33 && R[4] < 33 && R[5] < 33 && R[6] < 33 && R[7] < 33 && R[8] < 33) {
            grade.setText("F");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

